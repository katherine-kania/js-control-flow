// define the thing and assign a string
let thing = 'dog';

// define the count and assign a number
let count = 1;

// if the count is more than one, print the count and the thing with 's' added to the end of it - pluralazed thing
if (count > 1){
    console.log(`${count} ${thing}s`)

// if the count is one or less, print the count and thing as is
} else {
    console.log(`${count} ${thing}`)
}