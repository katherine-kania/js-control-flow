// es will print Hola mundo
// it will Ciao mondo
// pl will Witaj świecie
// all other inputs will print hello world

let language = 'pl'

if (language === "es"){
    console.log('Hola mundo')
} else if (language === 'it') {
    console.log('Ciao mondo')
} else if (language === 'pl') {
    console.log('Witaj świecie')
} else {
    console.log('Hello world')
}